function validateModel() {
  const evidenceIds = new Set(evidence.map((e) => e.id));
  const reqIds = new Set(requirements.map((r) => r.req_id));
  const problems = [];
  for (const r of requirements) {
    for (const id of r.source_ids) if (!evidenceIds.has(id)) problems.push(`${r.req_id} references missing evidence ${id}`);
  }
  for (const c of testCases) {
    if (!c.req_ids.length) problems.push(`${c.tc_id} has no req_ids`);
    if (!c.evidence_ids.length) problems.push(`${c.tc_id} has no evidence_ids`);
    for (const id of c.req_ids) if (!reqIds.has(id)) problems.push(`${c.tc_id} references missing req ${id}`);
    for (const id of c.evidence_ids) if (!evidenceIds.has(id)) problems.push(`${c.tc_id} references missing evidence ${id}`);
    if (!c.expected_result) problems.push(`${c.tc_id} has no expected_result`);
  }
  const uncovered = requirements.filter((r) => !testCases.some((c) => c.req_ids.includes(r.req_id)));
  for (const r of uncovered) problems.push(`${r.req_id} is uncovered`);
  if (problems.length) {
    throw new Error(`Model validation failed:\n${problems.join("\n")}`);
  }
}

function writeMatrix(sheet, startRow, startCol, headers, rows, tableName) {
  const matrix = [headers, ...rows];
  const ref = rangeRef(startRow, startCol, matrix.length, headers.length);
  const range = sheet.getRange(ref);
  range.values = matrix;
  range.format = {
    font: { name: "Arial", size: 10, color: "#1F2937" },
    borders: { preset: "all", style: "thin", color: "#D1D5DB" },
    verticalAlignment: "top",
    wrapText: true,
  };
  range.getRow(0).format = {
    fill: "#1F4E79",
    font: { name: "Arial", size: 10, color: "#FFFFFF", bold: true },
    horizontalAlignment: "center",
    verticalAlignment: "center",
    wrapText: true,
  };
  sheet.tables.add(ref, true).name = tableName;
  sheet.freezePanes.freezeRows(startRow);
  return ref;
}

function setWidths(sheet, widths) {
  widths.forEach((w, i) => {
    sheet.getRange(`${colName(i + 1)}:${colName(i + 1)}`).format.columnWidthPx = w;
  });
}

